#!/usr/bin/env node
/**
 * CI 校验脚本：API 生成质量检查
 *
 * 用途：
 * - 防止 Swagger 路径格式错误
 * - 确保模块输出隔离
 * - 强制全局 schemas 唯一
 * - 禁止手工修改 generated 文件
 *
 * 使用：
 *   node scripts/check-api-generation.mjs
 *
 * 退出码：
 *   0 - 所有检查通过
 *   1 - 至少一项检查失败
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const root = path.resolve(__dirname, '..')

let exitCode = 0

function fail(msg) {
  console.error(`❌  ${msg}`)
  exitCode = 1
}

function ok(msg) {
  console.log(`✅  ${msg}`)
}

// ========== 检查 1️⃣：Swagger 路径不含 Gin 风格的 /: ==========

function checkSwaggerNoColonParams() {
  console.log('\n🔍 检查 1️⃣：Swagger 路径不含 /: 参数...')

  const swaggerPath = path.resolve(
    root,
    '../Qingyu_backend/docs/api/swagger.yaml'
  )

  if (!fs.existsSync(swaggerPath)) {
    fail(`Swagger 文件不存在: ${swaggerPath}`)
    return
  }

  const content = fs.readFileSync(swaggerPath, 'utf8')

  // 匹配 Gin 风格的路径参数 /:param（不是 OpenAPI 的 {param}）
  // 负向先行断言确保 : 后面不是 {
  // 匹配 /: 后面跟着变量名的模式
  const colonPaths = content.match(/\/:(?![{])[a-zA-Z_][a-zA-Z0-9_]*/g)

  if (colonPaths) {
    fail(
      `发现 Gin 风格路径参数 /:param (应该是 OpenAPI 的 {param}):\n` +
        colonPaths.map((s) => `   ${s}`).join('\n')
    )
  } else {
    ok('Swagger 路径参数格式正确（使用 OpenAPI {param} 格式）。')
  }
}

// ========== 检查 2️⃣：模块输出隔离 ==========

function checkModuleOutputsIsolation() {
  console.log('\n🔍 检查 2️⃣：模块输出隔离...')

  const modules = ['reader', 'bookstore', 'admin', 'writer', 'social', 'ai', 'finance', 'notification']
  const orvalConfig = path.resolve(root, 'orval.config.ts')

  if (!fs.existsSync(orvalConfig)) {
    fail(`Orval 配置文件不存在: ${orvalConfig}`)
    return
  }

  const configContent = fs.readFileSync(orvalConfig, 'utf8')

  // 检查是否配置了 filters
  for (const mod of modules) {
    const hasFilter =
      configContent.includes(`${mod}:`) &&
      (configContent.includes(`paths: ['^/api/v1/${mod}/']`) ||
        configContent.includes(`paths: ['^/${mod}/']`) ||
        configContent.includes(`tags: ['${mod}']`))

    if (!hasFilter) {
      fail(`模块 ${mod} 未配置 input.filters，可能生成全量接口`)
    } else {
      ok(`模块 ${mod} 已配置路径过滤`)
    }
  }
}

// ========== 检查 3️⃣：全局 schemas 唯一性 ==========

function checkGlobalSchemasSingleton() {
  console.log('\n🔍 检查 3️⃣：全局 schemas 唯一性...')

  const globalSchema = path.resolve(root, 'src/api/generated/model.ts')

  // 必须存在全局 schemas
  if (!fs.existsSync(globalSchema)) {
    fail(`全局 schemas 缺失: ${globalSchema}`)
    return
  }

  ok(`全局 schemas 存在: ${globalSchema}`)

  // 扫描 modules 下是否还有 model.ts
  const modulesDir = path.resolve(root, 'src/modules')
  const offenders = []

  function walk(dir) {
    if (!fs.existsSync(dir)) return
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name)
      if (entry.isDirectory()) walk(full)
      if (
        entry.isFile() &&
        entry.name === 'model.ts' &&
        full.includes(`${path.sep}api${path.sep}generated${path.sep}`)
      ) {
        offenders.push(full)
      }
    }
  }

  walk(modulesDir)

  if (offenders.length > 0) {
    fail(
      `发现重复的 model.ts（只有全局 schemas 是允许的）:\n` +
        offenders.map((f) => `   - ${f}`).join('\n')
    )
  } else {
    ok('全局 schemas 唯一性检查通过（无模块级 model.ts）')
  }
}

// ========== 检查 4️⃣：generated 禁止手改 ==========

function checkGeneratedFilesHeader() {
  console.log('\n🔍 检查 4️⃣：generated 文件禁止手改...')

  const generatedRoots = [
    path.resolve(root, 'src/modules'),
    path.resolve(root, 'src/api/generated'),
  ]

  // 匹配任一生成标记
  const headerRegex = /(Generated by orval|@generated|DO NOT EDIT)/i
  const offenders = []

  function walk(dir) {
    if (!fs.existsSync(dir)) return
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name)
      if (entry.isDirectory()) walk(full)
      if (
        entry.isFile() &&
        entry.name.endsWith('.ts') &&
        full.includes(`${path.sep}generated${path.sep}`)
      ) {
        const content = fs.readFileSync(full, 'utf8').slice(0, 500)
        if (!headerRegex.test(content)) {
          offenders.push(full)
        }
      }
    }
  }

  generatedRoots.forEach(walk)

  if (offenders.length > 0) {
    fail(
      `以下 generated 文件缺少生成标记（可能被手工修改）:\n` +
        `   允许的标记：@generated | Generated by orval | DO NOT EDIT\n` +
        offenders.map((f) => `   - ${f}`).join('\n')
    )
  } else {
    ok('所有 generated 文件包含正确的生成标记')
  }
}

// ========== 主流程 ==========

function main() {
  console.log('🔍 运行 CI API 生成校验...\n')

  checkSwaggerNoColonParams()
  checkModuleOutputsIsolation()
  checkGlobalSchemasSingleton()
  checkGeneratedFilesHeader()

  console.log('\n' + '='.repeat(60))

  if (exitCode === 1) {
    console.error('❌ CI API 校验失败')
    console.error('\n💡 修复建议：')
    console.error('   1. 检查后端 Swagger 注释中的 @Router 路径格式')
    console.error('   2. 确保 orval.config.ts 中每个模块都有 filters')
    console.error('   3. 删除模块级的 model.ts，只保留 src/api/generated/model.ts')
    console.error('   4. 不要修改 generated 目录下的文件')
    process.exit(1)
  } else {
    console.log('✅ 所有 CI API 校验通过')
  }
}

main()
